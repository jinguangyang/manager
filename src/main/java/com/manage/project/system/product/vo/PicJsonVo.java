package com.manage.project.system.product.vo;

/**
 * 图片json
 * @author zhangjiawei
 *
 */
public class PicJsonVo {
	/**平台*/
	private String os;
	/**图片路径*/
	private String pic;
	/**图片大小*/
	private String px;
	/**图片类型*/
	private String type;

	public String getOs() {
		return os;
	}

	public void setOs(String os) {
		this.os = os;
	}

	public String getPic() {
		return pic;
	}

	public void setPic(String pic) {
		this.pic = pic;
	}

	public String getPx() {
		return px;
	}

	public void setPx(String px) {
		this.px = px;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	
}
