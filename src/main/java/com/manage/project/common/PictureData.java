package com.manage.project.common;

/**
 * 图片信息
 * 
 * @author zhangjiawei
 *
 */
public class PictureData {

	/**平台*/
	private String os;
	/**宽度*/
	private int width;
	/**高度*/
	private int height;
	
	public String getOs() {
		return os;
	}
	public void setOs(String os) {
		this.os = os;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
}
